#!/usr/bin/env python3
"""Measure classifier agreement against StockTwits author self-tags.

StockTwits posts carry a bullish/bearish tag chosen by their own author, and this
pipeline deliberately never lets the classifier see it (see sources/stocktwits.py).
That makes those posts a free held-out evaluation set: the classifier labels them
blind, and this script compares the two columns.

Read the number honestly. It is AGREEMENT WITH SELF-REPORTED INTENT, not accuracy
against objective truth:

  - The tags are self-reported by people talking their own book. Someone posting
    a bearish tag while quietly holding is noise we cannot see.
  - StockTwits offers no neutral tag, so this only scores the directional call.
    Neutral recall — the classifier's most common label — is unmeasured here.
  - StockTwits posts skew short and blunt. Reddit DD posts are longer and more
    hedged, so real Reddit accuracy is probably lower than this number suggests.

It is still worth having. "My classifier agrees with author intent 87% of the
time, measured on N held-out posts, and here are its confusions" is a defensible
claim. "The sentiment looks about right" is not.

Usage:
    python scripts/eval_classifier.py
"""

from __future__ import annotations

import sys
from collections import Counter
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent / "src"))

from sqlalchemy import select  # noqa: E402

from cashtag.db import Mention, session_scope  # noqa: E402


def main() -> None:
    with session_scope() as session:
        rows = session.execute(
            select(
                Mention.sentiment,
                Mention.author_sentiment,
                Mention.text,
                Mention.is_synthetic,
            ).where(
                Mention.author_sentiment.is_not(None),
                Mention.sentiment.is_not(None),
            )
        ).all()

    synthetic_rows = sum(1 for r in rows if r[3])
    if synthetic_rows:
        # Loud, because a number from seeded data is not a result. The seeder
        # invents both the label and the tag, so this score reflects the noise
        # rate chosen in seed_demo.py — nothing more. Quoting it as classifier
        # accuracy in an interview would not survive one follow-up question.
        print("!" * 62)
        print(
            f"WARNING: {synthetic_rows} of {len(rows)} scored posts are SYNTHETIC "
            "seed data.\nBoth the label and the 'author tag' were generated by "
            "scripts/seed_demo.py,\nso this score measures the seeder's noise "
            "parameter, not the classifier.\nIt is NOT a real result. Run the worker "
            "against live StockTwits data first."
        )
        print("!" * 62)
        print()

    if not rows:
        print(
            "No scoreable posts found.\n\n"
            "This needs posts with BOTH an author self-tag and a classifier label:\n"
            "  1. STOCKTWITS_ENABLED=true and the worker has run at least once\n"
            "  2. ANTHROPIC_API_KEY is set so classification actually ran\n\n"
            "Only StockTwits provides self-tags, and only ~35% of its posts carry one."
        )
        return

    total = len(rows)
    # StockTwits has no neutral tag, so a neutral prediction can never "agree".
    # Counting it as a miss would understate the classifier; counting it as a hit
    # would overstate it. It is reported separately as an abstention instead.
    directional = [(p, a) for p, a, _, _ in rows if p != "neutral"]
    neutral_predictions = total - len(directional)

    agree = sum(1 for pred, author in directional if pred == author)
    confusion: Counter = Counter((author, pred) for pred, author in directional)

    print("=" * 62)
    print("CLASSIFIER EVALUATION — agreement with StockTwits author self-tags")
    print("=" * 62)
    print(f"\nScoreable posts (author-tagged + classified): {total}")
    print(f"  Classifier abstained (predicted neutral):  {neutral_predictions}")
    print(f"  Directional predictions scored:            {len(directional)}")

    if not directional:
        print("\nNo directional predictions to score.")
        return

    accuracy = agree / len(directional) * 100
    print(f"\nDirectional agreement: {agree}/{len(directional)} = {accuracy:.1f}%")

    print("\nConfusion (author tag -> classifier label):")
    for (author, pred), count in sorted(confusion.items(), key=lambda kv: -kv[1]):
        mark = "OK " if author == pred else "MISS"
        print(f"  {mark}  {author:>8} -> {pred:<8}  {count:>5}")

    print("\nMisclassified examples:")
    shown = 0
    for pred, author, text, _synth in rows:
        if pred != "neutral" and pred != author and shown < 5:
            snippet = " ".join(text.split())[:100]
            print(f'  author={author:<8} model={pred:<8} "{snippet}..."')
            shown += 1
    if shown == 0:
        print("  (none)")

    print(
        "\nCaveat: this measures agreement with SELF-REPORTED intent on short, blunt "
        "\nStockTwits posts. It does not measure neutral recall, and Reddit DD posts "
        "\nare longer and more hedged — expect real-world accuracy to be lower."
    )


if __name__ == "__main__":
    main()
